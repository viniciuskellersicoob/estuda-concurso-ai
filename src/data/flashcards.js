// Função auxiliar para obter todos os flashcards
export function getAllFlashcards() {
    const all = [];
    Object.values(FLASHCARD_BANK).forEach(cards => {
        all.push(...cards);
    });
    return all;
}

// Função para obter flashcards por categoria
export function getFlashcardsByCategory(category) {
    return FLASHCARD_BANK[category] || [];
}

// Função para obter flashcards por exame
export function getFlashcardsByExam(examName) {
    const all = getAllFlashcards();
    return all.filter(card => card.exam.toLowerCase().includes(examName.toLowerCase()));
}

// Função para embaralhar flashcards
export function shuffleFlashcards(cards) {
    const shuffled = [...cards];
    for (let i = shuffled.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
    }
    return shuffled;
}
